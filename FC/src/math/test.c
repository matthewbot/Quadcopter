const static kalman_config conf = {
	{ // Q
		2.5E-9, 5E-7, 2.5E-8,
		5E-7, 1E-4, 5E-6,
		2.5E-8, 5E-6, 2.5E-7
	}, { // R
		.001, 0,
		0,  .005
	}
};

const float data[][3];

int main(int argc, char **argv) {
	kalman_filter filter;
	kalman_setup(&filter);
	
	int i;
	for (i=0; i<100; i++) {
		kalman_measurement measurement;
		measurement.fields.accelangle = atan2(data[i][2], data[i][1]) + M_PI/2;
		measurement.fields.gyrovel = data[i][0];
		
		kalman_step(&conf, &filter, &measurement);
	}
	
	printf("X: "); vector_print(filter.x.vec, 3);
	matrix_print(filter.P, 3, 3);
}

